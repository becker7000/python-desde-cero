
conjuntoA = {1,2,3,4}
conjuntoB = {3,4,5,6}

# ctrl + d (duplica la línea)
# ctrl + shift + arriba/abajo (mover líneas de código)
print("Conjunto A: ",conjuntoA)
print("Conjunto B: ",conjuntoB)

# Unión e intersección:
union_conjuntos = conjuntoA.union(conjuntoB)
inter_conjuntos = conjuntoA.intersection(conjuntoB)

print("Unión de A con B: ",union_conjuntos)
print("Intersección de A con B: ",inter_conjuntos)

