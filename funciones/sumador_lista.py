
def sumar_lista(lista):
    return sum(lista)

def calcular_promedio(lista):
    return sum(lista)/len(lista)

lista = [2.4,9.3,-1.0,4.5,4.8]

print(f" {lista}")
print(f" La suma de los numero es: {sumar_lista(lista):.3f}")
print(f" El promedio de los numero es: {calcular_promedio(lista):.3f}")

