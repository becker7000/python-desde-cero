
def sumar(a,b):
    return a+b

def restar(a,b):
    return a-b

def multiplar(a,b):
    return a*b

def dividir(a,b):
    return a//b

a = int(input("Escribe el valor de a: "))
b = int(input("Escribe el valor de b: "))

print(f" {a} + {b} = {sumar(a,b)}")
print(f" {a} - {b} = {restar(a,b)}")
print(f" {a} * {b} = {multiplar(a,b)}")
print(f" {a} / {b} = {dividir(a,b)}")

